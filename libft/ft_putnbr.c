#include "libft.h"

void	ft_putnbr(int nb)
{
	char			c;
	unsigned int	num;

	if (nb < 0)
	{
		write(1, "-", 1);
		num = (-1) * nb;
	}
	else
		num = nb;
	if (num > 9)
		ft_putnbr(num / 10);
	c = num % 10 + '0';
	write(1, &c, 1);
}
